namespace PlayListGenerator.Core.Models;

/// <summary>
///     Mp3 info object
/// </summary>
public class Mp3Info
{
    /// <summary>
    ///     album tag
    /// </summary>
    public string Album;

    /// <summary>
    ///     album sort tag
    /// </summary>
    public string AlbumSort;

    /// <summary>
    ///     artist tag
    /// </summary>
    public string Artist;

    /// <summary>
    ///     comment tag
    /// </summary>
    public string Comment;

    /// <summary>
    ///     duration property
    /// </summary>
    public int Duration;

    /// <summary>
    ///     genre tag
    /// </summary>
    public string Genre;

    /// <summary>
    ///     path tag
    /// </summary>
    public string Path;

    /// <summary>
    ///     tag id
    /// </summary>
    public string TagId;

    /// <summary>
    ///     title tag
    /// </summary>
    public string Title;

    /// <summary>
    ///     track tag
    /// </summary>
    public uint Track;

    /// <summary>
    ///     year tag
    /// </summary>
    public uint Year;
}